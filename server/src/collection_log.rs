#[derive(Deserialize, Serialize)]
#[serde(deny_unknown_fields)]
pub struct CollectionLog {
    #[serde(skip_serializing)]
    pub tab: i16,
    #[serde(skip_serializing)]
    pub page_name: String,
    #[serde(skip_deserializing)]
    pub page_id: i16,
    pub completion_counts: Vec<i32>,
    pub items: Vec<i32>,
    #[serde(skip_deserializing)]
    pub new_items: Vec<i32>
}

#[derive(Serialize)]
#[derive(Clone)]
pub struct CollectionLogInfo {
    pub tabs: Vec<(i16, String)>,
    pub pages: Vec<(i16, i16, String, Vec<&'static str>)>,
    pub items: Vec<(i16, i32)>,
    #[serde(skip_serializing)]
    page_name_to_id_lookup: HashMap<String, i16>,
    #[serde(skip_serializing)]
    page_id_item_set_lookup: HashMap<i16, HashSet<i32>>,
    #[serde(skip_serializing)]
    item_name_to_id_lookup: HashMap<String, i32>,
    #[serde(skip_serializing)]
    item_id_to_page_id_lookup: HashMap<i32, HashSet<i16>>
}

impl CollectionLogInfo {
    pub fn new(tabs: Vec<(i16, String)>, pages_db: Vec<(i16, i16, String)>, items: Vec<(i16, i32)>) -> Self {
        let mut page_name_to_id_lookup = HashMap::new();
        for page in &pages_db {
            page_name_to_id_lookup.insert(page.2.clone(), page.1);
        }

        let mut page_id_item_set_lookup = HashMap::new();
        let mut item_id_to_page_id_lookup = HashMap::new();
        for item in &items {
            if !page_id_item_set_lookup.contains_key(&item.0) {
                page_id_item_set_lookup.insert(item.0, HashSet::new());
            }

            match page_id_item_set_lookup.get_mut(&item.0) {
                Some (x) => x.insert(item.1),
                None => true
            };

            if !item_id_to_page_id_lookup.contains_key(&item.1) {
                item_id_to_page_id_lookup.insert(item.1, HashSet::new());
            }

            match item_id_to_page_id_lookup.get_mut(&item.1) {
                Some (x) => x.insert(item.0),
                None => true
            };
        }

        let mut item_name_to_id_lookup = HashMap::new();
        for item in COLLECTION_LOG_ITEMS.iter() {
            item_name_to_id_lookup.insert(item.2.clone(), item.3);
        }

        let mut pages: Vec<(i16, i16, String, Vec<&'static str>)> = Vec::new();
        for page in pages_db {
            match COLLECTION_TAB_PAGES.iter().find(|&x| x.1 == page.2) {
                Some (x) => pages.push((page.0, page.1, page.2, x.2.clone())),
                None => ()
            };
        }

        Self { tabs, pages, items, page_name_to_id_lookup, page_id_item_set_lookup, item_name_to_id_lookup, item_id_to_page_id_lookup }
    }

    pub fn page_name_to_id(&self, page_name: &String) -> Option<&i16> {
        self.page_name_to_id_lookup.get(page_name)
    }

    pub fn has_item(&self, page_id: i16, item_id: i32) -> bool {
        match self.page_id_item_set_lookup.get(&page_id) {
            None => false,
            Some (x) => x.contains(&item_id)
        }
    }

    pub fn item_name_to_id(&self, item_name: &String) -> Option<&i32> {
        self.item_name_to_id_lookup.get(item_name)
    }

    pub fn page_ids_for_item(&self, item_id: i32) -> Option<&HashSet<i16>> {
        self.item_id_to_page_id_lookup.get(&item_id)
    }

    pub fn number_of_items_in_page(&self, page_id: i16) -> usize {
        match self.page_id_item_set_lookup.get(&page_id) {
            None => 0,
            Some (x) => x.len()
        }
    }

}

